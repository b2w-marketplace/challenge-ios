import UIKit

extension UIColor {
    
    class func appPurple(_ alpha: CGFloat = 1.0) -> UIColor {
        return UIColor.init(red: 106.0/255.0, green: 62.0/255.0, blue: 145.0/255.0, alpha: alpha)
    }
    
}

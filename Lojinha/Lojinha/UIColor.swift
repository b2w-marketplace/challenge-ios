import UIKit

extension UIColor {
    
    func darkishPurpleNavBar() -> UIColor {
        return UIColor(red: 90/255, green: 41/255, blue: 134/255, alpha: 0.9)
    }
    
    func dark() -> UIColor {
        return UIColor(red: 45/255, green: 49/255, blue: 66/255, alpha: 1)
    }
    
    func tomato() -> UIColor {
        return UIColor(red: 241/255, green: 80/255, blue: 37/255, alpha: 1)
    }
    
    func greyishBrown() -> UIColor {
        return UIColor(red: 74/255, green: 74/255, blue: 74/255, alpha: 1)
    }
    
    func greyish() -> UIColor {
        return UIColor(red: 170/255, green: 170/255, blue: 170/255, alpha: 1)
    }
    
    func warmPurple() -> UIColor {
        return UIColor(red: 94/255, green: 42/255, blue: 132/255, alpha: 1)
    }
    
    func warmGreySeparator() -> UIColor {
        return UIColor(red: 151/255, green: 151/255, blue: 151/255, alpha: 0.2)
    }
}
